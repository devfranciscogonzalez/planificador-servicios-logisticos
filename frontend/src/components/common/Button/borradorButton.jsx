import { LoadingButton } from '@mui/lab';
import CheckCircleIcon from '@mui/icons-material/CheckCircle';

const AcceptButton = ({
  label = "Aceptar",
  type = "submit",
  onClick,
  startIcon = <CheckCircleIcon />,
  loading = false,
  disabled = false,
  sx = {},
}) => {
  return (
    <LoadingButton
      type={type}
      variant="contained"
      color="primary"
      onClick={onClick}
      startIcon={startIcon}
      loading={loading}
      loadingPosition="start"
      disabled={disabled}
      sx={{ mx: 1, ...sx }}
    >
      {label}
    </LoadingButton>
  );
};

export default AcceptButton;
